package Modelos;

/**
 *
 * @author Jelson Fernandes
 */
public class ModeloTabelaPaciMedico {
    private String cod;
    private String nome;

    public ModeloTabelaPaciMedico(String cod, String nome) {
        this.cod = cod;
        this.nome = nome;
    }

    /**
     * @return the cod
     */
    public String getCod() {
        return cod;
    }

    /**
     * @param cod the cod to set
     */
    public void setCod(String cod) {
        this.cod = cod;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
