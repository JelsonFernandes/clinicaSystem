package Modelos;

/**
 *
 * @author Jelson Fernandes
 */
public class ModeloUsuarios {
    private int CodUsu;
    private String NomeUsu;
    private String PerfilUsu;
    private String EmailUsu;
    private String SenhaUsu;

    /**
     * @return the CodUsu
     */
    public int getCodUsu() {
        return CodUsu;
    }

    /**
     * @param CodUsu the CodUsu to set
     */
    public void setCodUsu(int CodUsu) {
        this.CodUsu = CodUsu;
    }

    /**
     * @return the NomeUsu
     */
    public String getNomeUsu() {
        return NomeUsu;
    }

    /**
     * @param NomeUsu the NomeUsu to set
     */
    public void setNomeUsu(String NomeUsu) {
        this.NomeUsu = NomeUsu;
    }

    /**
     * @return the PerfilUsu
     */
    public String getPerfilUsu() {
        return PerfilUsu;
    }

    /**
     * @param PerfilUsu the PerfilUsu to set
     */
    public void setPerfilUsu(String PerfilUsu) {
        this.PerfilUsu = PerfilUsu;
    }

    /**
     * @return the EmailUsu
     */
    public String getEmailUsu() {
        return EmailUsu;
    }

    /**
     * @param EmailUsu the EmailUsu to set
     */
    public void setEmailUsu(String EmailUsu) {
        this.EmailUsu = EmailUsu;
    }

    /**
     * @return the SenhaUsu
     */
    public String getSenhaUsu() {
        return SenhaUsu;
    }

    /**
     * @param SenhaUsu the SenhaUsu to set
     */
    public void setSenhaUsu(String SenhaUsu) {
        this.SenhaUsu = SenhaUsu;
    }

    
}
